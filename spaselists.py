def extract_definitions(list_name, specific_list_data, dictionary_data):
    """
    Extracts and prints definitions for values in a given list from the dictionary data.
    Returns True if any individual allowedValue has a definition, False otherwise.

    Args:
        list_name (str): The name of the list being processed.
        specific_list_data (dict): Dictionary containing 'allowedValues' and its own 'definition'.
        dictionary_data (dict): The main dictionary containing definitions.
    """
    has_individual_definitions = False
    output_lines = []

    if 'allowedValues' in specific_list_data and isinstance(specific_list_data['allowedValues'], list):
        list_definition = specific_list_data.get('definition', 'No overall list definition found.')
        output_lines.append(f"\n--- Definitions for '{list_name}' ---")
        output_lines.append(f"Overall list definition: {list_definition}")
        output_lines.append(f"\nIndividual value descriptions within '{list_name}':")

        for value in specific_list_data['allowedValues']:
            if value in dictionary_data:
                definition = dictionary_data[value].get('definition', None)
                if definition:
                    output_lines.append(f"  - {value}: {definition}")
                    has_individual_definitions = True
                else:
                    output_lines.append(f"  - {value}: No 'definition' key found in its dictionary entry.")
            else:
                output_lines.append(f"  - {value}: No specific entry in the main dictionary.")
    else:
        output_lines.append(f"'allowedValues' not found or is not a list in the data for '{list_name}'.")

    if has_individual_definitions:
        for line in output_lines:
            print(line)
    return has_individual_definitions

