"""### Generate JSON-LD for a List

Now, let's create JSON-LD (JSON for Linking Data) for one of the lists, 'AccessRights', using the definitions we've extracted. JSON-LD helps structure data on the web for better machine readability and search engine optimization, linking it to well-known vocabularies like schema.org. This will mimic the style of the `AccessRights.json` example provided, with 'Isabella Ruiz' as the author.
"""

import json
import requests

# Define the chosen list name (already available from previous cells)
chosen_list_name = cleaned_list_name

# Retrieve data for the chosen list
list_json_ld = {}

# Use the already extracted list_definition_dict for overall list definition
overall_definition = list_definition_dict.get(chosen_list_name, 'No overall list definition found.')

# Use the already extracted definitions_dict for individual allowed values and their definitions
allowed_values_and_definitions = definitions_dict

# Define the base ID for the ConceptScheme
concept_scheme_id = f"https://spase-group.org/data/model/spase-latest/skos/{chosen_list_name}"

# Prepare the JSON-LD for the overall list using SKOS vocabulary
list_json_ld = {
    "@context": {
        "skos": "http://www.w3.org/2004/02/skos/core#",
        "dct": "http://purl.org/dc/terms/", # Added for creator mapping
        "Concept": { "@id": "skos:Concept" },
        "ConceptScheme": { "@id": "skos:ConceptScheme" }, # Modified to map to skos:ConceptScheme
        "title": { "@id": "skos:prefLabel" },  # Map 'title' to 'skos:prefLabel'
        "description": { "@id": "skos:definition" }, # Map 'description' to 'skos:definition'
        "creator": { "@id": "dct:creator" }, # Map 'creator' to 'dct:creator'
        "prefLabel": { "@id": "skos:prefLabel" }, # Kept for concepts within hasTopConcept
        "definition": { "@id": "skos:definition" }, # Kept for concepts within hasTopConcept
        "inScheme": { "@id": "skos:inScheme" }
    },
    "@graph": [ # Start of the @graph array
        {
            "@id": concept_scheme_id,
            "@type": "ConceptScheme", # This will resolve to skos:ConceptScheme via @context
            "title": chosen_list_name, # Using 'title' as per user example
            "description": overall_definition, # Using 'description' as per user example
            "creator": "Isabella Ruiz"
        }
    ]
}

# Add each allowedValue as a skos:Concept
# Append to the hasTopConcept list within the first item of the @graph array
for value, individual_definition in allowed_values_and_definitions.items():
    concept_id = f"{concept_scheme_id}/{value}"

    list_json_ld["@graph"].append({
        "@type": "Concept", # This will resolve to skos:Concept via @context
        "prefLabel": value, # Uses the 'prefLabel' mapping from context
        "definition": individual_definition, # Uses the 'definition' mapping from context
        "inScheme": concept_scheme_id # Link back to the concept scheme
    })

print(json.dumps(list_json_ld, indent=2))

